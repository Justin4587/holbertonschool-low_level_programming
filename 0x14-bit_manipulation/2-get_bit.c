/**
* get_bit - function
* description: print
* @index: position
* @n: lists
* Return: size
*/

int get_bit(unsigned long int n, unsigned int index)
{
  int status;  

  if (index > 63)
    {
      return (-1);
    }

  status = (n >> index) & 1;



  return (status);
}
 
